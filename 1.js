// Example: Using variables and assignment in JavaScript

// Declaring variables
let name;      // variable without value
let age = 20;  // variable with initial assignment

// Assigning values later
name = "Arpit";  

// Using const (value cannot be reassigned)
const country = "India";

// Reassigning variables
age = 21;   // age changed from 20 to 21

// Printing values
console.log("Name:", name);
console.log("Age:", age);
console.log("Country:", country);
